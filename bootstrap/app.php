<?php

use Illuminate\Foundation\Application;
use Illuminate\Foundation\Configuration\Exceptions;
use Illuminate\Foundation\Configuration\Middleware;
use Illuminate\Validation\ValidationException;

return Application::configure(basePath: dirname(__DIR__))
    ->withRouting(
        web: __DIR__.'/../routes/web.php',
        api: __DIR__.'/../routes/api.php',
        commands: __DIR__.'/../routes/console.php',
        health: '/up',
    )
    ->withMiddleware(function (Middleware $middleware): void {
        $middleware->use([
            \Illuminate\Http\Middleware\TrustProxies::class,
            \Illuminate\Http\Middleware\HandleCors::class,
        ]);

        $middleware->alias([
            'basic.auth' => \App\Http\Middleware\BasicAuthMiddleware::class,
            'log.inout' => \App\Http\Middleware\LogRequestInOutMiddleware::class,
        ]);
    })
    ->withExceptions(function (Exceptions $exceptions): void {

        $exceptions->render(function (Illuminate\Database\Eloquent\ModelNotFoundException $e, Request $request) {
            if ($request->is('api/*')) {
                // set response from macro response configuration
                return Response::notFound($e, "Data not found");
            }
        });

        // Handle Laravel's default NotFoundHttpException
        $exceptions->render(function (Symfony\Component\HttpKernel\Exception\NotFoundHttpException $e, Illuminate\Http\Request $request) {
            if ($request->is('api/*')) {
                // set response from macro response configuration
                return Response::notFound($e, "Endpoint not found");
            }
        });

        // Handle Laravel's default validation exception
        $exceptions->render(function (ValidationException $e, Illuminate\Http\Request $request) {
            if ($request->expectsJson()) {
                // set response from macro response configuration
                return Response::unproccessableContent($e->errors());
            }
        });
        
        // Handle laravel's default server error exception
        $exceptions->render(function (Throwable $e, Illuminate\Http\Request $request) {
            if ($request->is('api/*')) {
                // set response from macro response configuration
                return response()->internalServerError($e, $e->getMessage());
            }
        });
    })->create();
